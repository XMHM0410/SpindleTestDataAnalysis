import pandas as pd
import numpy as np
# 采样率
fs = 200000.0
# 转速
rpm = 12000.0
# 需要截取的圈数
loops = 20
# 每秒转过的圈数
rps = rpm / 60.0
# 转过loops圈所用的时间
time = loops / rps
# 采样点数
n = time * fs
# 打印
print(n)
